function zoneIdByType(venue: Venue | null, zoneType: string, fallbackIndex = 0): string | null {
  if (!venue) {
    return null;
  }

  const matches = venue.zones.filter(
    (zone) => (zone.zone_type ?? zone.zoneType ?? '').toLowerCase() === zoneType
  );

  return matches[fallbackIndex]?.id ?? venue.zones[fallbackIndex]?.id ?? null;
}

