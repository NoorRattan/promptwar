export function buildDemoZoneDensities(venue: Venue | null): ZoneDensity[] {
  if (!venue) {
    return [];
  }

  return venue.zones.map((zone) => {
    const density = Number(zone.current_density ?? zone.currentDensity ?? 0);
    const level = String(zone.density_level ?? zone.densityLevel ?? 'LOW').toUpperCase() as ZoneDensity['level'];
    const lat = zone.lat_center ?? zone.latCenter ?? 0;
    const lng = zone.lng_center ?? zone.lngCenter ?? 0;

    return {
      id: zone.id,
      zoneId: zone.id,
      venueId: venue.id,
      venue_id: venue.id,
      name: zone.name,
      zoneName: zone.name,
      zone_name: zone.name,
      zone_type: zone.zone_type ?? zone.zoneType ?? null,
      density,
      current_density: density,
      level,
      density_level: level,
      count: Number(zone.current_count ?? zone.currentCount ?? 0),
      isOpen: Boolean(zone.is_open ?? zone.isOpen ?? true),
      is_open: Boolean(zone.is_open ?? zone.isOpen ?? true),
      updatedAt: null,
      updated_at: null,
      latCenter: lat,
      lat_center: lat,
      lngCenter: lng,
      lng_center: lng,
      polygon: zone.polygon ?? null,
      center_point: { latitude: lat, longitude: lng },
    };
  });
}
