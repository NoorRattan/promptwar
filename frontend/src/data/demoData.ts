export function getDemoVenue(override?: Partial<Venue> | null): Venue {
  const id = override?.id ?? DEMO_VENUE.id;
  const lat =
    override?.lat_center ?? override?.latCenter ?? DEMO_VENUE.lat_center ?? DEMO_VENUE.latCenter;
  const lng =
    override?.lng_center ?? override?.lngCenter ?? DEMO_VENUE.lng_center ?? DEMO_VENUE.lngCenter;
  const floorPlanUrl =
    override?.floor_plan_url ?? override?.floorPlanUrl ?? DEMO_VENUE.floor_plan_url ?? null;
  const floorPlanBounds =
    override?.floor_plan_bounds ?? override?.floorPlanBounds ?? DEMO_VENUE.floor_plan_bounds ?? null;
  const isActive = override?.is_active ?? override?.isActive ?? DEMO_VENUE.is_active ?? true;

  return {
    ...DEMO_VENUE,
    ...override,
    id,
    slug: override?.slug ?? DEMO_VENUE.slug,
    name: override?.name ?? DEMO_VENUE.name,
    address: override?.address ?? DEMO_VENUE.address,
    city: override?.city ?? DEMO_VENUE.city,
    country: override?.country ?? DEMO_VENUE.country,
    capacity: override?.capacity ?? DEMO_VENUE.capacity,
    latCenter: lat,
    lat_center: lat,
    lngCenter: lng,
    lng_center: lng,
    floorPlanUrl,
    floor_plan_url: floorPlanUrl,
    floorPlanBounds,
    floor_plan_bounds: floorPlanBounds,
    timezone: override?.timezone ?? DEMO_VENUE.timezone,
    isActive,
    is_active: isActive,
    zones: getDemoVenueZones(id),
  };
}

